                    Console.Clear();
                    break;
                default:
                    slow.PrintSlowly("Invalid choice.");
                    break;
            }
        }
        public void UseHealthPotion(Guts guts)
        {
            if (HealthPotion > 0)
            {
                if (guts.Health > 150)
                {
                    slow.PrintSlowly("Are you sure you want to use this potion? You can't have more than 200 HP. Y/N");
                    string choice = Console.ReadLine();
                    if (choice == "Y" || choice == "y")
                    {
                        guts.Health = 200;
                        HealthPotion--;
                        slow.PrintSlowly("You have used a Health Potion.");
                        slow.PrintSlowly($"You have {guts.Health} HP.");
                    }
                    else if (choice == "N" || choice == "n")
                    {
                        slow.PrintSlowly("You have chosen not to use the Health Potion.");
                    }
                    else
                    {
                        slow.PrintSlowly("Invalid choice.");
                    }
                }
                else if (guts.Health <= 150)
                {
                    guts.Health += 50;
                    HealthPotion -= 1;
                    slow.PrintSlowly("You have used a Health potion.");
                    slow.PrintSlowly($"You have {guts.Health} HP.");
                }
            }
            else
            {
                slow.PrintSlowly("You have no health potions left.");
            }
        }

